# int and float
# float stores binaries on L and R sides of decimal in different locations in memort
# float more memory expensive than int

print(type(2 + 2))

print(type(2 - 4))
print(type(2 / 4))
print(type(9 + 1.2))
print(
    type(9.8 + 1.2)
)  # still a float even though it total a whole because decimal point kept

print(2**2)  # exponential
print(5 // 4)  # 1 --> // is hard divide
print(4 // 5)  # 0 as it is less than 1
print(5 % 4)
